  /// Helper method to get monospace font for technical data
  /// Uses JetBrains Mono for measurements and coordinates
  static TextStyle getMonospaceStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    final Color textColor = isLight ? textPrimaryLight : textPrimaryDark;

    return GoogleFonts.jetBrainsMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: textColor,
      letterSpacing: 0,
    );
  }

  /// Helper method to get accent colors for status indicators
  static Color getStatusColor(String status, {required bool isLight}) {
    switch (status.toLowerCase()) {
      case 'success':
      case 'completed':
      case 'exported':
        return isLight ? successLight : successDark;
      case 'warning':
      case 'processing':
      case 'pending':
        return isLight ? accentLight : accentDark;
      case 'error':
      case 'failed':
      case 'invalid':
        return isLight ? errorLight : errorDark;
      default:
        return isLight ? textSecondaryLight : textSecondaryDark;
    }
  }
